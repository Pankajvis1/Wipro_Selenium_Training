package Looping_Statements;

public class Each_for {

	public static void main(String[] args) {
		
		int[] arr = {6, 3, 1, 24, 6};
		
		for(int a : arr) {
			System.out.println(a);
		}
		System.out.println("*71836187381719*");
		
		for(int i = 0; i < arr.length; i++) {
			System.out.println("arr[i]");
		}
		
	}

}
